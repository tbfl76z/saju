def get_extended_saju_data(details):
    """
    sajupy.get_saju_details의 결과에 십성, 12운성, 오행 정보를 추가합니다.
    """
    pillars = details['pillars']
    day_gan = pillars['day']['stem']
    
    # 십성 계산
    ten_gods = {
        'year': GAN_TEN_GODS[day_gan].get(pillars['year']['stem'], '-'),
        'month': GAN_TEN_GODS[day_gan].get(pillars['month']['stem'], '-'),
        'day': '본인',
        'hour': GAN_TEN_GODS[day_gan].get(pillars['hour']['stem'], '-')
    }
    
    # 지지 십성 (지장간 정기 기준)
    jiji_ten_gods = {
        'year': GAN_TEN_GODS[day_gan].get(BRANCH_HIDDEN_GANS.get(pillars['year']['branch']), '-'),
        'month': GAN_TEN_GODS[day_gan].get(BRANCH_HIDDEN_GANS.get(pillars['month']['branch']), '-'),
        'day': GAN_TEN_GODS[day_gan].get(BRANCH_HIDDEN_GANS.get(pillars['day']['branch']), '-'),
        'hour': GAN_TEN_GODS[day_gan].get(BRANCH_HIDDEN_GANS.get(pillars['hour']['branch']), '-')
    }
    
    # 12운성 계산
    twelve_growth = {
        'year': TWELVE_GROWTH[day_gan].get(pillars['year']['branch'], '-'),
        'month': TWELVE_GROWTH[day_gan].get(pillars['month']['branch'], '-'),
        'day': TWELVE_GROWTH[day_gan].get(pillars['day']['branch'], '-'),
        'hour': TWELVE_GROWTH[day_gan].get(pillars['hour']['branch'], '-')
    }
    
    # 오행 분포 계산
    five_elements = {'목': 0, '화': 0, '토': 0, '금': 0, '수': 0}
    for p in ['year', 'month', 'day', 'hour']:
        stem = pillars[p]['stem']
        branch = pillars[p]['branch']
        five_elements[ELEMENTS_MAP.get(stem, '')] = five_elements.get(ELEMENTS_MAP.get(stem, ''), 0) + 1
        five_elements[ELEMENTS_MAP.get(branch, '')] = five_elements.get(ELEMENTS_MAP.get(branch, ''), 0) + 1
    
    # 결과 업데이트
    details['ten_gods'] = ten_gods
    details['jiji_ten_gods'] = jiji_ten_gods
    details['twelve_growth'] = twelve_growth
    details['five_elements'] = five_elements
    
    return details
